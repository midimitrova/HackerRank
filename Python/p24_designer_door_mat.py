n, m = input().split()
m1 = int(m)
n1 = int(n)

print("-" * ((m1 // 2) - 1) + ".|." * 1 + "-" * ((m1 // 2) - 1))
for i in range(1, (n1 // 2)):
    print("-" * ((m1 // 2) - (i * 3) - 1) + ".|." * ((i * 2) + 1) + "-" * ((m1 // 2) - (i * 3) - 1))
print("-" * ((m1 // 2) - 3) + "WELCOME" + "-" * ((m1 // 2) - 3))
for k in range(n1 // 2, 0, -1):
    print("-" * ((m1 // 2) - (k * 3) + 2) + ".|." * ((k * 2) - 1) + "-" * ((m1 // 2) - (k * 3) + 2))
